    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped
        char c = evt.getKeyChar();
        if(!(Character.isAlphabetic(c) ||  (c == KeyEvent.VK_BACK_SPACE)||  c==KeyEvent.VK_DELETE || c==KeyEvent.VK_SPACE))
        evt.consume();
    }//GEN-LAST:event_txtNombreKeyTyped

    private void tbShowCategoriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbShowCategoriaMouseClicked
        int fila = tbShowCategoria.getSelectedRow();
        try
        {
            int id = listaidCategoria[fila];
            Categoria categoria = new Categoria();
            CategoriaDAO cdao = new CategoriaDAO();
            categoria = cdao.buscarEntidad(id);
            txtNombre.setText(categoria.getNombre());
            estado = categoria.getEstado();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_tbShowCategoriaMouseClicked

    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String usu= txtNombre.getText();
        estado = 1;
        usu=usu.replaceAll(" ", "");
        if(usu.length()==0)
        {
            JOptionPane.showMessageDialog(this,"INGRESE NOMBRE DE CATEGORIA");
        }
        else
        {
            try
            {
                Categoria categoria= new Categoria();
                categoria.setNombre(txtNombre.getText());
                categoria.setEstado(estado);
                CategoriaDAO carDAO = new CategoriaDAO();
                carDAO.crearEntidad(categoria);
                ListarCategoria();
                JOptionPane.showMessageDialog(this,"Se Agrego Exitosamente");
            }
            catch (Exception e)
            {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int row = tbShowCategoria.getSelectedRow();
        if (row == -1)
        {
            JOptionPane.showMessageDialog(this,"Seleccione un elemento para poder eliminar");
        }
        else
        {
            try
            {
                int fila = tbShowCategoria.getSelectedRow();
                int id = listaidCategoria[fila];
                CategoriaDAO cdao = new CategoriaDAO();
                cdao.eliminarEntidad(id);
                ListarCategoria();
                JOptionPane.showMessageDialog(this,"Se Elimino Exitosamente");
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int row = tbShowCategoria.getSelectedRow();
        if (row == -1)
        {
            JOptionPane.showMessageDialog(this,"Seleccione un elemento para modificar");
        }
        else
        {
            String usu= txtNombre.getText();
            usu=usu.replaceAll(" ", "");
            if(usu.length()==0 )
            {
                JOptionPane.showMessageDialog(this,"INGRESE NOMBRE");
            }
            else
            {
                try
                {
                    int fila = tbShowCategoria.getSelectedRow();
                    int idCategoria = listaidCategoria[fila];
                    Categoria categoria = new Categoria();
                    categoria.setIdCategoria(idCategoria);
                    categoria.setNombre(txtNombre.getText());
                    categoria.setEstado(estado);
                    CategoriaDAO cdao = new CategoriaDAO();
                    cdao.actualizarEntidad(categoria);
                    ListarCategoria();
                    JOptionPane.showMessageDialog(this,"Se Modifico Exitosamente");
                }
                catch(Exception e)
                {
                    JOptionPane.showMessageDialog(this,e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnModificarActionPerformed


