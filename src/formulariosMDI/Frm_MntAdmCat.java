    public void ListarCategoria()
        {
        DefaultTableModel dtm=(DefaultTableModel)this.tbShowCategoria.getModel();
        dtm.setColumnCount(0);
        dtm.setRowCount(0);
        CategoriaDAO cdao = new CategoriaDAO();
        ArrayList<Categoria> lc = cdao.listarCategoria();
        listaidCategoria = new int[lc.size()];
        String col[] = {"Nombre"};
        dtm.setColumnIdentifiers(col);
        Object[] row = new Object[dtm.getColumnCount()];        
        for (int i = 0; i < lc.size(); i++) {
            listaidCategoria[i] = lc.get(i).getIdCategoria();            
            row[0] = lc.get(i).getNombre();             
            dtm.addRow(row);
        }
    }

