    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int index = cbxTipo.getSelectedIndex();
        int tipo = listaTipo[index];
        int index1 = cbxEstado.getSelectedIndex();
        int estado = listaEstado[index1];

        String usu= txtUsuario.getText();
        String pass= txtPassword.getText();
        usu=usu.replaceAll(" ", "");
        pass=pass.replaceAll(" ", "");

        if(usu.length()==0 || pass.length()==0)
        {

            JOptionPane.showMessageDialog(this,"INGRESE DATOS : NOMBRE y/o PASSWORD");
        }
        else
        {
            try
            {
                Usuario usuario= new Usuario();
                usuario.setUsuario(txtUsuario.getText());
                usuario.setPassword(txtPassword.getText());
                usuario.setEstado(estado);
                usuario.setTipo(tipo);
                UsuarioDAO uDAO = new UsuarioDAO();
                uDAO.crearEntidad(usuario);
                ListarUsuario();
                JOptionPane.showMessageDialog(this,"Se Agrego Exitosamente");
            }
            catch (Exception e)
            {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int row = tbShowUsuario.getSelectedRow();

        if (row == -1)
        {
            JOptionPane.showMessageDialog(this,"Seleccione un elemento para poder eliminar");
        }
        else
        {
            try
            {
                int fila = tbShowUsuario.getSelectedRow();
                int id = listaidUsuario[fila];
                UsuarioDAO udao = new UsuarioDAO();
                udao.eliminarEntidad(id);
                ListarUsuario();
                JOptionPane.showMessageDialog(this,"Se Elimino Exitosamente");
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int row = tbShowUsuario.getSelectedRow();

        if (row == -1)
        {
            JOptionPane.showMessageDialog(this,"Seleccione un elemento para modificar");
        }
        else
        {
            String usu= txtUsuario.getText();
            String pass= txtPassword.getText();
            usu=usu.replaceAll(" ", "");
            pass=pass.replaceAll(" ", "");

            if(usu.length()==0 || pass.length()==0)
            {

                JOptionPane.showMessageDialog(this,"INGRESE DATOS : NOMBRE y/o PASSWORD");
            }
            else
            {
                try{
                    int index = cbxTipo.getSelectedIndex();
                    int tipo = listaTipo[index];
                    int index1 = cbxEstado.getSelectedIndex();
                    int estado = listaEstado[index1];
                    int fila = tbShowUsuario.getSelectedRow();
                    int idUsuario = listaidUsuario[fila];

                    Usuario usuario = new Usuario();
                    usuario.setIdUsuario(idUsuario);
                    usuario.setUsuario(txtUsuario.getText());
                    usuario.setPassword(txtPassword.getText());
                    usuario.setTipo(tipo);
                    usuario.setEstado(estado);

                    UsuarioDAO udao = new UsuarioDAO();
                    udao.actualizarEntidad(usuario);
                    ListarUsuario();
                    JOptionPane.showMessageDialog(this,"Se Modifico Exitosamente");

                }
                catch(Exception e)
                {
                    JOptionPane.showMessageDialog(this,e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnModificarActionPerformed


