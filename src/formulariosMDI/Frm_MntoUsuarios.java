    public void ListarUsuario(){
        DefaultTableModel dtm=(DefaultTableModel)this.tbShowUsuario.getModel();
        dtm.setColumnCount(0);
        dtm.setRowCount(0);
        UsuarioDAO pdao = new UsuarioDAO();
        ArrayList<Usuario> lc = pdao.listarEntidad();
        listaidUsuario = new int[lc.size()];
        String col[] = {"IdUsuario","Nombre","Password","Tipo","Estado"};
        dtm.setColumnIdentifiers(col);
        Object[] row = new Object[dtm.getColumnCount()];        
        for (int i = 0; i < lc.size(); i++) {
            listaidUsuario[i] = lc.get(i).getIdUsuario();
            row[0] = lc.get(i).getIdUsuario();
            row[1] = lc.get(i).getUsuario();
            row[2] = lc.get(i).getPassword();
            row[3] = lc.get(i).getTipo();   
            row[4] = lc.get(i).getEstado();
            dtm.addRow(row);
        }
    }

