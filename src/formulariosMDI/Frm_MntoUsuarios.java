    private void tbShowUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbShowUsuarioMouseClicked
        int fila = tbShowUsuario.getSelectedRow();

        try
        {

            int id = listaidUsuario[fila];
            Usuario usuario = new Usuario();
            UsuarioDAO uDAO = new UsuarioDAO();

            usuario = uDAO.buscarEntidad(id);
            txtUsuario.setText(usuario.getUsuario());
            txtPassword.setText(usuario.getPassword());

            if(listaTipo[0] == usuario.getTipo())
            {
                cbxTipo.setSelectedIndex(0);
            }
            if(listaTipo[1] == usuario.getTipo())
            {
                cbxTipo.setSelectedIndex(1);
            }
            if(listaEstado[0] == usuario.getEstado())
            {
                cbxEstado.setSelectedIndex(0);
            }
            if(listaEstado[1] == usuario.getEstado())
            {
                cbxEstado.setSelectedIndex(1);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_tbShowUsuarioMouseClicked

